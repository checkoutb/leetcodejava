package pojo;


/**
 * Definition for a point.
 * class Point {
 *     int x;
 *     int y;
 *     Point() { x = 0; y = 0; }
 *     Point(int a, int b) { x = a; y = b; }
 * }
 */
public class Point
{
    public int x;
    public int y;

    public Point()
    {
        x = 0;
        y = 0;
    }

    public Point(int a, int b)
    {
        x = a;
        y = b;
    }
}
